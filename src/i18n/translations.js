export function getMediaCategory(category, language) {
  return translations[language]?.media.categories[category] ?? category
}

export function getMediaTitle(title, language) {
  const patterns = translations[language]?.media.titlePatterns ?? translations.en.media.titlePatterns
  const match = title.match(/^(Restaurant Story|Restaurant Film|AI Study|AI Film|Campaign Flyer|Behind the Scenes) (\d+)$/)

  if (!match) return title

  const [, type, number] = match
  const key = {
    'Restaurant Story': 'restaurantStory',
    'Restaurant Film': 'restaurantFilm',
    'AI Study': 'aiStudy',
    'AI Film': 'aiFilm',
    'Campaign Flyer': 'campaignFlyer',
    'Behind the Scenes': 'behindScenes',
  }[type]

  return `${patterns[key]} ${number}`
}

export function getMediaAlt(item, language, imageKind = 'image') {
  const alt = translations[language]?.media.alt ?? translations.en.media.alt
  const number = item.displayIndex ?? item.title.match(/(\d+)$/)?.[1] ?? ''

  if (item.id.startsWith('restaurant-photo')) return `${alt.restaurantPhoto} ${number}`.trim()
  if (item.id.startsWith('restaurant-film')) return `${alt.restaurantFilm} ${number}`.trim()
  if (item.id.startsWith('ai-image')) return `${alt.aiImage} ${number}`.trim()
  if (item.id.startsWith('ai-film')) return `${alt.aiFilm} ${number}`.trim()
  if (item.id.startsWith('flyer')) return `${alt.flyer} ${number}`.trim()
  if (item.id.startsWith('bts')) return `${alt.behindScenes} ${number}`.trim()

  return imageKind === 'poster' ? item.posterAlt : item.alt
}
